    @TestCases(IntlMoneyFormatterTest.decimalExamples)
    @Test("it formats money as decimal")
    public itFormatsMoneyAsDecimal(
        amount: string,
        currencyCode: string,
        subunit: number,
        minFractionDigits: number,
        maxFractionDigits: number,
        expected: string,
    ) {
        const money = new RoundedMoney(amount, subunit, new Currency(currencyCode));

        const formatter = new IntlMoneyFormatter({
            locales: "en-AU",
            style: "decimal",
            minFractionDigits,
            maxFractionDigits,
            normaliseWhitespace: true,
        });

        const actual = formatter.format(money);
        Expect(actual).toBe(expected);
    }

